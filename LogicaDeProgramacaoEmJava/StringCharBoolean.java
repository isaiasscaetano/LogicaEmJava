package LogicaDeProgramacaoEmJava;

public class StringCharBoolean {
    public static void main(String[] args) {
        char caractere = 100;
        char feminino = 'f';
        char masculino = 'm';

        System.out.println(caractere);
        System.out.println(feminino);
        System.out.println(masculino);
        System.out.println(feminino + masculino);
        System.out.println(masculino + feminino);

        String Feminino = "F";
        String Masculino = "M";
        String nome = "aqui pode escrever um texto muito grande";

        System.out.println(nome);
        System.out.println(Feminino);
        System.out.println(Masculino);
        System.out.println(Feminino + Masculino);

        boolean condiçao = false;
        boolean condicao = true;

        System.out.println(condiçao);
        System.out.println(condicao);


    }


}
